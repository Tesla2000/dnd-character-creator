def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--depth",
        type=int,
        default=3,
        help="Max chain depth to test (default: 3)",
    )
    parser.add_argument(
        "--verbose", action="store_true", help="Print each state checked"
    )
    args = parser.parse_args()

    unit_failures = run_unit_tests()
    integration_result = run_validation(max_depth=args.depth, verbose=args.verbose)

    total_failures = unit_failures + integration_result
    if total_failures == 0:
        sys.stdout.write("\nAll checks passed.\n")
    else:
        sys.stdout.write(f"\n{total_failures} check(s) failed.\n")
    return 0 if total_failures == 0 else 1


if __name__ == "__main__":
    sys.exit(main())
