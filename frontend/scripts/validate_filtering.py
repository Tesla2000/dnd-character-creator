class _ArgSpec(TypedDict, total=False):
    kind: str
    name: str
    values: list[object]
    origin: str
    args: list[_ArgSpec]
    members: list[object]


class _BlueprintSig(TypedDict):
    origin: str
    args: list[_ArgSpec]


class _BlockSig(TypedDict, total=False):
    input: _BlueprintSig
    output: _BlueprintSig


class _State(TypedDict):
    origin: str
    args: list[_ArgSpec]


class _BlockInfo(TypedDict, total=False):
    class_name: str
    blueprint_sig: _BlockSig


class _Failure(TypedDict):
    context: str
    state: str
    only_frontend: list[str]
    only_python: list[str]


class _MetaJson(TypedDict):
    emptyBlueprintArgs: list[_ArgSpec]
    originHierarchy: dict[str, list[str]]


