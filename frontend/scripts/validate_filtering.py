def run_validation(max_depth: int = 3, verbose: bool = False) -> int:
    sys.stdout.write(
        f"Running frontend filtering validation (max_depth={max_depth})...\n"
    )
    failures: list[_Failure] = []
    total = 0

    empty_state: _State = {"origin": "Blueprint", "args": empty_blueprint_args}

    # Warm up the sig cache for all blocks up front
    for cls_name in class_map:
        _cached_sig(cls_name)

    def recurse(state: _State, path: list[str], depth: int) -> None:
        nonlocal total
        total += 1
        context = " -> ".join(path) if path else "initial"
        compare_at_state(state, context, failures, verbose=verbose)

        if depth >= max_depth:
            return

        # Only recurse into blocks compatible with both systems (intersection)
        ts_compat = compatible_set_json(state)
        py_compat = compatible_set_py(state)
        reachable = sorted(ts_compat | py_compat)  # include both for full coverage

        for cls_name in reachable:
            b_info = block_by_class.get(cls_name)
            if b_info is None:
                continue

            # Compute next state with each system separately — they should agree
            next_json = apply_block_json(state, b_info)
            next_py = apply_block_py_state(state, cls_name)

            # If the states differ, track both to expose downstream differences
            if next_json != next_py:
                # Detect state divergence — report as a separate issue
                total += 1
                sig_j = json.dumps(next_json.get("args", []), sort_keys=True)
                sig_p = json.dumps(next_py.get("args", []), sort_keys=True)
                if sig_j != sig_p:
                    failures.append(
                        {
                            "context": f"STATE DIVERGE after {context} -> {cls_name}",
                            "state": f"json={_fmt_state(next_json)}\n       py={_fmt_state(next_py)}",
                            "only_frontend": [],
                            "only_python": [],
                        }
                    )

            # Recurse using the json-based next state (canonical path)
            recurse(next_json, path + [cls_name], depth + 1)

    recurse(empty_state, [], 0)

    # ------------------------------------------------------------------
    # Print results
    # ------------------------------------------------------------------
    sep = "=" * 72

    sys.stdout.write(f"\n{sep}\n")
    sys.stdout.write(f"States tested : {total}\n")
    sys.stdout.write(f"Failures      : {len(failures)}\n")
    sys.stdout.write(f"{sep}\n")

    if not failures:
        sys.stdout.write(
            "RESULT: PASS — frontend filtering exactly matches Python type system\n"
        )
        return 0

    sys.stdout.write("RESULT: FAIL — differences found:\n\n")
    for i, f in enumerate(failures, 1):
        sys.stdout.write(f"  [{i}] {f['context']}\n")
        sys.stdout.write(f"       state  = {f['state']}\n")
        if f["only_frontend"]:
            sys.stdout.write(
                f"       frontend-only (may be over-permissive): {f['only_frontend']}\n"
            )
        if f["only_python"]:
            sys.stdout.write(
                f"       python-only   (may be over-permissive): {f['only_python']}\n"
            )
        sys.stdout.write("\n")

    return 1


