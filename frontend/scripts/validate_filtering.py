def _fmt_spec(spec: _ArgSpec) -> str:
    """Compact representation of a JSON arg spec for diff output."""
    k = spec.get("kind", "?")
    if k == "none":
        return "None"
    if k == "free":
        return "_"
    if k == "literal":
        return f"Literal{spec['values']}"
    if k == "type":
        return str(spec["name"])
    if k == "generic":
        args_list = spec.get("args", [])
        inner = ", ".join(_fmt_spec(a) for a in args_list)
        return f"{spec['origin']}[{inner}]"
    return str(spec)


def _fmt_state(state: _State) -> str:
    args_str = ", ".join(_fmt_spec(a) for a in state["args"])
    return f"{state['origin']}[{args_str}]"


def compare_at_state(
    state: _State,
    context: str,
    failures: list[_Failure],
    verbose: bool = False,
) -> bool:
    ts_compat = compatible_set_json(state)
    py_compat = compatible_set_py(state)

    only_json = ts_compat - py_compat
    only_py = py_compat - ts_compat

    if only_json or only_py:
        failures.append(
            {
                "context": context,
                "state": _fmt_state(state),
                "only_frontend": sorted(only_json),
                "only_python": sorted(only_py),
            }
        )
        return False
    if verbose:
        sys.stdout.write(f"  OK [{len(ts_compat)} blocks]: {context}\n")
    return True


