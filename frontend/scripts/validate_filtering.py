# Blueprint subclass registry for origin checks
_BLUEPRINT_SUBCLASS_REGISTRY: dict[str, type] = {"Blueprint": Blueprint}
for _cls in _all_block_classes:
    for _c in _cls.__mro__:
        if isinstance(_c, type) and issubclass(_c, Blueprint) and _c is not Blueprint:
            _BLUEPRINT_SUBCLASS_REGISTRY[_c.__name__] = _c
# Add known Blueprint subclasses that don't appear directly in block MROs
for _bp_cls in (
    PresentableBasicBlueprint,
    SorcererBlueprint,
    WizardBlueprint,
    WizardLevel18Blueprint,
    WizardLevel20Blueprint,
):
    _BLUEPRINT_SUBCLASS_REGISTRY[_bp_cls.__name__] = _bp_cls


def _get_block_sig_as_json(cls: type[BuildingBlock]) -> _BlockSig | None:
    """
    Produce the same 19-arg JSON sig as generate_blocks.py for a block class.
    This is the 'Python truth' — fully resolved type annotations.
    """
    apply_fn = next(
        (c.__dict__["apply"] for c in cls.__mro__ if "apply" in c.__dict__), None
    )
    if apply_fn is None:
        return None
    try:
        hints = typing.get_type_hints(apply_fn)
    except Exception:
        return None
    bindings = _collect_typevar_bindings(cls)
    input_sig = _sig_from_hint(hints.get("blueprint"), bindings)
    output_sig = _sig_from_hint(hints.get("return"), bindings)
    if input_sig is None and output_sig is None:
        return None
    result: _BlockSig = {}
    if input_sig is not None:
        result["input"] = input_sig  # type: ignore[typeddict-item]
    if output_sig is not None:
        result["output"] = output_sig  # type: ignore[typeddict-item]
    return result


def arg_compatible_py(current: _ArgSpec, expected: _ArgSpec) -> bool:
    """
    Python-native arg compatibility check mirroring the frontend logic exactly.

    Uses Python issubclass for origin checks but otherwise mirrors arg_compatible_json,
    including the 'literal matches type' fix and recursive generic handling.
    Note: _arg_compatible from _validate_pipeline.py is NOT used here because it
    (a) lacks the literal-matches-type fix and (b) doesn't recurse into inner generic
    args with the fix applied. Differences with _validate_pipeline.py are documented
    as known bugs in that module.
    """
    if expected["kind"] == "free":
        return True
    # literal value IS compatible with a class type bound (e.g. Literal[0] ~ FirstSubclassPreLevel)
    if expected["kind"] == "type" and current["kind"] == "literal":
        return True
    if current["kind"] != expected["kind"]:
        return False
    if current["kind"] == "none":
        return True
    if current["kind"] == "type":
        c_name = str(current["name"])
        e_name = str(expected["name"])
        c_cls = _TYPE_REGISTRY.get(c_name) or class_map.get(c_name)
        e_cls = _TYPE_REGISTRY.get(e_name) or class_map.get(e_name)
        if isinstance(c_cls, type) and isinstance(e_cls, type):
            return issubclass(c_cls, e_cls)
        return bool(current["name"] == expected["name"])
    if current["kind"] == "literal":
        expected_set = {str(v) for v in expected["values"]}
        return all(str(v) in expected_set for v in current["values"])
    if current["kind"] == "generic":
        c_origin = _GENERIC_REGISTRY.get(str(current["origin"]))
        e_origin = _GENERIC_REGISTRY.get(str(expected["origin"]))
        if c_origin is None or e_origin is None:
            if current["origin"] != expected["origin"]:
                return False
        elif c_origin is not e_origin:
            if isinstance(c_origin, type) and isinstance(e_origin, type):
                if not issubclass(c_origin, e_origin):
                    return False
            else:
                return False
        cur_args = current["args"]
        exp_args = expected["args"]
        return all(
            arg_compatible_py(cur_args[i], e)
            for i, e in enumerate(exp_args)
            if i < len(cur_args)
        )
    return False


def is_sub_origin_py(current_name: str, expected_name: str) -> bool:
    current_cls = _BLUEPRINT_SUBCLASS_REGISTRY.get(current_name)
    expected_cls = _BLUEPRINT_SUBCLASS_REGISTRY.get(expected_name)
    if current_cls is None or expected_cls is None:
        return current_name == expected_name
    return issubclass(current_cls, expected_cls)


# Cache block sigs to avoid repeated computation
_sig_cache: dict[str, _BlockSig | None] = {}


def _cached_sig(cls_name: str) -> _BlockSig | None:
    if cls_name not in _sig_cache:
        cls = class_map.get(cls_name)
        _sig_cache[cls_name] = _get_block_sig_as_json(cls) if cls else None
    return _sig_cache[cls_name]


def is_compatible_py(cls_name: str, state: _State) -> bool:
    sig = _cached_sig(cls_name)
    if not sig or not sig.get("input"):
        return True
    inp: _BlueprintSig = sig["input"]
    if not is_sub_origin_py(str(state["origin"]), str(inp["origin"])):
        return False
    state_args = state["args"]
    inp_args = inp["args"]
    pair = min(len(state_args), len(inp_args))
    return all(arg_compatible_py(state_args[j], inp_args[j]) for j in range(pair))


def apply_block_py_state(state: _State, cls_name: str) -> _State:
    if not is_compatible_py(cls_name, state):
        return state
    sig = _cached_sig(cls_name)
    if not sig or not sig.get("output"):
        return state
    out: _BlueprintSig = sig["output"]
    state_args2 = state["args"]
    out_args = out["args"]
    new_args: list[_ArgSpec] = [
        state_args2[j] if a["kind"] == "free" and j < len(state_args2) else a
        for j, a in enumerate(out_args)
    ]
    return {"origin": out["origin"], "args": new_args}


def compatible_set_py(state: _State) -> set[str]:
    return {cls_name for cls_name in class_map if is_compatible_py(cls_name, state)}


