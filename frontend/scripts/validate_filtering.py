#!/usr/bin/env python3
"""
Validate that the frontend block filtering matches the Python type system.

Two comparison systems:
  1. JSON-based (frontend port): uses blocks-generated.json + TypeScript argCompatible logic
  2. Python-native: uses actual Python type hints with full TypeVar resolution,
     deserialized back to Python objects and checked with _arg_compatible

Reports differences per test state and gives a final pass/fail verdict.

Usage:
  python3 frontend/scripts/validate_filtering.py
  python3 frontend/scripts/validate_filtering.py --depth 3   # default
  python3 frontend/scripts/validate_filtering.py --depth 2   # faster
"""

