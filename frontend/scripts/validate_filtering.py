def run_unit_tests() -> int:
    sys.stdout.write("\nRunning arg_compatible unit tests...\n")
    errors: list[str] = []

    def check(label: str, got: bool, expected: bool) -> None:
        if got != expected:
            errors.append(f"  FAIL {label}: got {got}, expected {expected}")
        else:
            sys.stdout.write(f"  ok   {label}\n")

    free_spec: _ArgSpec = {"kind": "free"}
    none_spec: _ArgSpec = {"kind": "none"}
    lit0: _ArgSpec = {"kind": "literal", "values": [0]}
    lit1: _ArgSpec = {"kind": "literal", "values": [1]}
    type_race: _ArgSpec = {"kind": "type", "name": "Race"}
    type_first_pre: _ArgSpec = {"kind": "type", "name": "FirstSubclassPreLevel"}

    wiz_pre0: _ArgSpec = {
        "kind": "generic",
        "origin": "WizardPreSubclassLevel",
        "args": [lit0, none_spec],
    }
    wiz_pre1: _ArgSpec = {
        "kind": "generic",
        "origin": "WizardPreSubclassLevel",
        "args": [lit1, none_spec],
    }
    sorc_pre0: _ArgSpec = {
        "kind": "generic",
        "origin": "SorcererPreSubclassLevel",
        "args": [lit0, none_spec],
    }

    # -- free always matches --
    check(
        "free expected, any current -> True (json)",
        arg_compatible_json(lit0, free_spec),
        True,
    )
    check(
        "free expected, any current -> True (py)",
        arg_compatible_py(lit0, free_spec),
        True,
    )

    # -- none matches none --
    check(
        "none vs none -> True (json)", arg_compatible_json(none_spec, none_spec), True
    )
    check("none vs none -> True (py)", arg_compatible_py(none_spec, none_spec), True)

    # -- none does NOT match wizard level generic --
    check(
        "none vs WizPreSubLvl -> False (json)",
        arg_compatible_json(none_spec, wiz_pre0),
        False,
    )
    check(
        "none vs WizPreSubLvl -> False (py)",
        arg_compatible_py(none_spec, wiz_pre0),
        False,
    )

    # -- same generic matches same generic --
    check(
        "wiz_pre0 vs wiz_pre0 -> True (json)",
        arg_compatible_json(wiz_pre0, wiz_pre0),
        True,
    )
    check(
        "wiz_pre0 vs wiz_pre0 -> True (py)", arg_compatible_py(wiz_pre0, wiz_pre0), True
    )

    # -- different level does NOT match --
    check(
        "wiz_pre1 vs wiz_pre0 -> False (json)",
        arg_compatible_json(wiz_pre1, wiz_pre0),
        False,
    )
    check(
        "wiz_pre1 vs wiz_pre0 -> False (py)",
        arg_compatible_py(wiz_pre1, wiz_pre0),
        False,
    )

    # -- literal matches type (THE KEY FIX for sorcerer) --
    check(
        "lit0 vs FirstSubclassPre -> True (json)",
        arg_compatible_json(lit0, type_first_pre),
        True,
    )
    check(
        "lit0 vs FirstSubclassPre -> True (py)",
        arg_compatible_py(lit0, type_first_pre),
        True,
    )

    # -- literal matches any type bound (by design: enum values match their class bound) --
    # Note: the fix is intentionally broad — we accept Literal[n] vs any type class
    # in practice this only fires at positions where enum literals live (level slots)
    check("lit0 vs Race -> True (json)", arg_compatible_json(lit0, type_race), True)
    check("lit0 vs Race -> True (py)", arg_compatible_py(lit0, type_race), True)

    # -- wrong class generic is False --
    check(
        "sorc_pre0 vs wiz_pre0 -> False (json)",
        arg_compatible_json(sorc_pre0, wiz_pre0),
        False,
    )
    check(
        "sorc_pre0 vs wiz_pre0 -> False (py)",
        arg_compatible_py(sorc_pre0, wiz_pre0),
        False,
    )

    # -- free inner matches concrete inner --
    wiz_sub_free_subclass: _ArgSpec = {
        "kind": "generic",
        "origin": "WizardSubclassLevel",
        "args": [lit1, free_spec],
    }
    wiz_sub_concrete: _ArgSpec = {
        "kind": "generic",
        "origin": "WizardSubclassLevel",
        "args": [lit1, _ArgSpec(kind="type", name="AnyWizardLevel")],
    }
    check(
        "WizardSubLvl[1,free] vs WizardSubLvl[1,concrete] -> True (json)",
        arg_compatible_json(wiz_sub_concrete, wiz_sub_free_subclass),
        True,
    )
    check(
        "WizardSubLvl[1,free] vs WizardSubLvl[1,concrete] -> True (py)",
        arg_compatible_py(wiz_sub_concrete, wiz_sub_free_subclass),
        True,
    )

    sys.stdout.write(f"\n  Unit tests: {14 - len(errors)} / 14 passed\n")
    for err in errors:
        sys.stdout.write(f"{err}\n")
    return len(errors)


